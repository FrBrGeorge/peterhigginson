<h1>ARMlite Documentation</h1>
<p>ARMlite simulates a simple computer built around a cut-down version of a 32-bit ARM processor. It was extended from Peter's earlier simulators specifically to meet the needs of a book written by Richard Pawson. Richard's help in developing the simulator, particularly the graphics, is gratefully acknowledged.</p>
<h3>Releases</h3>
<p>You came here from the v1.0.0 release (the initial release). The latest release is <a href="index.html" target="_blank">here</a>.</p>
<p>The latest release has a varying Program area width (depending on the browser window), aligning of comments, scaling of the binary mode display and a more complete implementation of the ARM shift instructions.</p>
<h3>ARMlite Programming Reference Manual</h3>
<p>The features and assembly language supported by ARMlite are documented in the <a href="Programming&#32;reference&#32;manual.pdf" target="_blank">ARMlite Programming Reference Manual</a>.</p>
<p>For instructions on how to use the simulator and set breakpoints see page 8 of <a href="Assembly&#32;Language&#32;-&#32;Student&#32;version.pdf" target="_blank">Richard's book</a>.</p>
<h3>The Book</h3>
<p>Assembly Language Programming by Richard Pawson with Peter Higginson, is a textbook written specifically for teaching A-level. The student version is freely available <a href="Assembly&#32;Language&#32;-&#32;Student&#32;version.pdf" target="_blank">here</a>.  There is also a Teacher version available, which includes model answers to all exercises, and additional Teacher Notes. The latter is available through the Computing At School forum <a href="https://community.computingatschool.org.uk/resources/6019/single" target="_blank">here</a>, or by emailing Richard Pawson (rpawson at metalup.org).</p>
<h3>The AQA Assembly Language</h3>
<p>Appendix I of the book explains the restrictions that need to be followed when using ARMlite to keep to instruction set used by the AQA in previous examinations. In particular the byte vs. word addressing issues need to be understood. A copy of just the Appendix is <a href="Appendix&#32;I.pdf" target="_blank">here</a>.</p>
<h3>Experimental Features</h3>
<p>ARMlite has a small number of experimental features to enable more advanced programs to be written. They are documented <a href="ARMlite&#32;Experimental&#32;Features.pdf" target="_blank">here</a>.</p>
